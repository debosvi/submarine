uint8_t dm_get_nb_bytes(const uint8_t nb) {
    uint8_t _ret=(nb>>DM_MODULO_SHIFT_SHIFT);
    if(nb-(DM_MODULO_SHIFT_BASIC*_ret)) _ret++;
    return _ret;
}
